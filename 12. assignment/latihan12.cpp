#include <iostream>

using namespace std;

int main(){

    int a = 12;
    cout << "nilai awal adalah " << a << endl;

    // ditambah
    a += 2;
    cout << "ditambah 2 : " << a << endl;

    //dikurang
    a -= 4;
    cout << "dikurang 4 : " << a << endl;

    //dikali
    a *= 2;
    cout << "dikali 2 :" << a << endl;

    //dibagi
    a /= 5;
    cout << "dibagi 5 : " << a << endl;


    return 0;
}